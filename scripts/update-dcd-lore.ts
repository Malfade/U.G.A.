import "dotenv/config";
import { PrismaClient } from "../src/generated/prisma/client.js";
import { PrismaPg } from "@prisma/adapter-pg";

const url = process.env.DATABASE_URL;
if (!url) throw new Error("DATABASE_URL is not set");

const prisma = new PrismaClient({
  adapter: new PrismaPg({ connectionString: url }),
});

async function updateEvent(
  name: string,
  data: {
    newName?: string;
    date?: string;
    description?: string;
    cause?: string;
    consequences?: string;
    accessLevel?: number;
  }
) {
  const row = await prisma.event.findFirst({ where: { name } });
  if (!row) {
    console.warn(`  ⚠ событие не найдено: ${name}`);
    return;
  }
  await prisma.event.update({
    where: { id: row.id },
    data: {
      name: data.newName ?? name,
      date: data.date,
      description: data.description,
      cause: data.cause,
      consequences: data.consequences,
      accessLevel: data.accessLevel,
    },
  });
  console.log(`  ✓ событие: ${data.newName ?? name}`);
}

async function updateDocument(
  title: string,
  data: { newTitle?: string; content?: string; accessLevel?: number }
) {
  const row = await prisma.document.findFirst({ where: { title } });
  if (!row) {
    console.warn(`  ⚠ документ не найден: ${title}`);
    return;
  }
  await prisma.document.update({
    where: { id: row.id },
    data: {
      title: data.newTitle ?? title,
      content: data.content,
      accessLevel: data.accessLevel,
    },
  });
  console.log(`  ✓ документ: ${data.newTitle ?? title}`);
}

async function ensureEvent(
  name: string,
  data: {
    date: string;
    description: string;
    cause?: string;
    consequences?: string;
    accessLevel?: number;
  }
) {
  const existing = await prisma.event.findFirst({ where: { name } });
  if (existing) {
    await prisma.event.update({ where: { id: existing.id }, data });
    console.log(`  ✓ событие (обновлено): ${name}`);
    return existing;
  }
  const created = await prisma.event.create({ data: { name, ...data } });
  console.log(`  + событие (создано): ${name}`);
  return created;
}

async function deleteEvent(name: string) {
  const row = await prisma.event.findFirst({ where: { name } });
  if (!row) return;
  await prisma.event.delete({ where: { id: row.id } });
  console.log(`  − удалено: ${name}`);
}

